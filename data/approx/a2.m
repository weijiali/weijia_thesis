x=0:1:1;
y=x;
[X,Y]=meshgrid(x,y);
figure1=figure();
axes1 = axes('Parent',figure1);
view(axes1,[107.5 24]);
grid(axes1,'on');
hold(axes1,'all');
Z=X.*Y;
h(1)=surf(X,Y,Z,'Parent',axes1);
hold on;
Z2=(X+Y)/2;
h(2)=surf(X,Y,Z2,'Parent',axes1);
hold off;
set(h(1),'FaceColor',[0.9529 0.8706 0.7333]);
set(h(2),'FaceColor',[0.7294 0.8314 0.9569]);
xlabel('X');
ylabel('Y');
zlabel('Z');
legend('Z=X*Y','Z=(X+Y)/2');
rotate3d on;
saveas(gcf,'../../figures/f1.eps','eps');
